class ifelseif
  {
    public static void main(String [] args)
    {
      String name="vamsi";  
      
    if(name.equals("Bala")){  
        System.out.println("name is Bala");  
    }  
    else if(name.equals("Ramesh")){  
        System.out.println("name is ramesh");  
    }  
    else if(name.equals("siddu")){  
        System.out.println("name is siddu");  
    }  
    else if(name.equals("rani")){  
        System.out.println("name is rani");  
    }  
    else if(name.equals("suresh")){  
        System.out.println("name is suresh");  
    }else if(name.equals("ammula")){  
        System.out.println("name is ammula");  
    }else{  
        System.out.println("Name is not found");  
    }  
}  
} 
      