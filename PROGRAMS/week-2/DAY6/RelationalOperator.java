class RelationalOperator
  {
    public static void main(String args[])
    {
      System.out.println("lessthan(<) "+(90<56));
      System.out.println("lessthanorequal(<=) "+(56<=52));
      System.out.println("greaterthan(>) "+(90>56));
      System.out.println("greaterthanorequal(>=) "+(90>=56));
      System.out.println("equaltoequalto(==) "+(50==50));
      System.out.println("notequalto(!=) "+(50!=60));
    }
  }